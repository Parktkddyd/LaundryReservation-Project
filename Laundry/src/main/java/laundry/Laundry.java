package laundry;

public class Laundry {
	private int laundryNo;
	private int laundryModel;
	private String laundryStartTime;
	private String laundryEndTime;
	private int laundryAvailable;
	
	public int getLaundryNo() {
		return laundryNo;
	}
	public void setLaundryNo(int laundryNo) {
		this.laundryNo = laundryNo;
	}
	public int getLaundryModel() {
		return laundryModel;
	}
	public void setLaundryModel(int laundryModel) {
		this.laundryModel = laundryModel;
	}
	public String getLaundryStartTime() {
		return laundryStartTime;
	}
	public void setLaundryStartTime(String laundryStartTime) {
		this.laundryStartTime = laundryStartTime;
	}
	public String getLaundryEndTime() {
		return laundryEndTime;
	}
	public void setLaundryEndTime(String laundryEndTime) {
		this.laundryEndTime = laundryEndTime;
	}
	public int getLaundryAvailable() {
		return laundryAvailable;
	}
	public void setLaundryAvailable(int laundryAvailable) {
		this.laundryAvailable = laundryAvailable;
	}
}
